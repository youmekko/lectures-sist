package com.test;

public class Sample021 {

	public static void main(String[] args) {
		
		//비교 연산자
		// ==, != , >, <, >=, <=
		//-> 결과는 반드시 true or false
		
		int a = 10;
		int b = 20;
		
		System.out.println( a == b );
		System.out.println( a != b );
		System.out.println( a > b );
		System.out.println( a < b );
		System.out.println( a >= b );
		System.out.println( a <= b );
		
	}

}
